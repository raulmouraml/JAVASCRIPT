function soma(n1=0, n2=0){ /* Criado a afunção "soma" com os parâmetros n1 e n2 
"Caso o valor de n1 ou n2 não seja digitado o mesmo será igual a zero"*/
    return n1 + n2  /* A função retornará n1 + n2*/
}
